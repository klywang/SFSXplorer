# last line indicates the type of binding affinity. The chklig.in is as follows:
#
# #Type of binding information: ki
#
# CHKLIG,1DWB,BEN,H, 1, 2.92
#
# CHKLIG,1ETR,MIT,H, 1, 7.40
#
#...
#
# The first column is a keyword to indicate that this line brings ligand data. 
# The second column shows the PDB access code, followed by the ligand id, ligand
# chain, ligand number, and the binding affinity. The first line shows the type 
# of binding affinity. This chklig.in file follows the format generated by
# the program SAnDReS (Xavier et al., 2016).
#
# Walter F. de Azevedo Jr.
# July 27, 2019
# https://azevedolab.net
#
# References:
# Morris G, Goodsell D, Halliday R, Huey R, Hart W, Belew R, Olson A. Automated
# docking using a Lamarckian genetic algorithm and an empirical binding free 
# energy function. J Comput Chem. 1998; 19:1639-1662. 
#
# Xavier MM, Heck GS, de Avila MB, Levin NM, Pintro VO, Carvalho NL, 
# Azevedo WF Jr. SAnDReS a Computational Tool for Statistical Analysis of 
# Docking Results and Development of Scoring Functions. Comb Chem High 
# Throughput Screen. 2016; 19(10): 801â€“812. 
# PMID: 27686428 DOI: 10.2174/1386207319666160927111347
# https://www.ncbi.nlm.nih.gov/pubmed/27686428
#
